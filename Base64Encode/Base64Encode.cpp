DWORD TOOL_Buffer_to_File(char* Buffer, DWORD dwSize, const WCHAR* FilePath)
{
    //---------------------------------------写入文件
    HANDLE NFile = 0;
    DWORD dwRetBuf = 0;
    do
    {

        NFile = CreateFile(FilePath, GENERIC_ALL, FILE_SHARE_READ, NULL, CREATE_ALWAYS, FILE_ATTRIBUTE_NORMAL, FALSE);

    } while (GetLastError() == ERROR_ALREADY_EXISTS && DeleteFile(FilePath));

    //
    if (GetLastError() == ERROR_ACCESS_DENIED)
    {

        MessageBox(NULL, L"出现错误，请用管理员权限重试", L"ERROR!", 0);
        return 0;
    }

    //生成的是ShellCode
    WriteFile(NFile, Buffer, dwSize, &dwRetBuf, FALSE);

    CloseHandle(NFile);
    return dwRetBuf;
}
